# Fitting MPN data to lognormal-binomial 
cat(
  "model{
    for (i in 1:5320) {
      c[i] ~ dbin(p[i],3)
      p[i] <- 1-exp(-lambda[i]*v[i])
      lambda[i] ~ dlnorm(mu[i],tau_all)
      mu[i] <- b0 + b1*gear[i] + b2*tide[i] + b3*gear[i]*tide[i] + b4*mod[i] + b5*hi[i] + U[samp[i]] + V[time[i]]
      zlog.like[i] <- -log(sum(dbin(c[i],p[i],3)))
    }
    for (s in 1:996) {
      U[s] ~ dnorm(0,tau_U)
    }
    for (t in 1:20) {
      V[t] ~ dnorm(0,tau_V)
    }
    tau_U ~ dgamma(0.1,0.1)
    tau_V ~ dgamma(0.1,0.1)
    tau_all ~ dgamma(0.1,0.1)
    b0 ~ dnorm(0,0.1)
    b1 ~ dnorm(0,0.1)
    b2 ~ dnorm(0,0.1)
    b3 ~ dnorm(0,0.1)
    b4 ~ dnorm(0,0.1)
    b5 ~ dnorm(0,0.1)
  }",
  file="m1.jag"
)

